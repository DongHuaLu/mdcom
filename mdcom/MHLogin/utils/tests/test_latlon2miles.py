from django.test import TestCase
from MHLogin.utils.latlon2mile import get_distance


class GetDistanceTest(TestCase):

	def test_get_distance(self):
		data = [
			[47.669498, -117.401001, 37.358765, -121.860021, 747],
			[37.4852152, -122.2363548, 37.442994, -122.152324, 5],

			[47.669498, -117.401001, 37.4852152, -122.2363548, 745],
			[37.358765, -121.860021, 37.358765, -121.860021, 0],
			[47.669498, 117.401001, 37.358765, 121.860021, 747],
			[37.358765, -121.860021, 7.669498, 7.401001, 7924],
			[7.669498, 7.401001, 37.358765, -121.860021, 7924],

			[0, 0, 37.358765, -121.860021, 7932],
			[0, 0, 0, 0, 0],
			[90, -180, 37.358765, -121.860021, 3637],
			[47.669498, -117.401001, 90, 180, 2925], 
			[-90, -180, 37.358765, -121.860021, 8800],
			[47.669498, -117.401001, -90, -180, 9512],
			[90, 180, 37.358765, -121.860021, 3637],
		]
		for d in data:
			self.assertEqual(round(get_distance(d[0], d[1], d[2], d[3]), 0), d[4])

